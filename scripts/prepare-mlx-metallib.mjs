import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptPath = fileURLToPath(import.meta.url);
const repoRoot = path.resolve(path.dirname(scriptPath), "..");
const srcTauriDir = path.join(repoRoot, "src-tauri");
const bundledMetallibPath = path.join(srcTauriDir, "resources", "mlx.metallib");

if (process.platform !== "darwin") {
  console.log("Skipping mlx.metallib preparation on non-macOS host.");
  process.exit(0);
}

const profile = process.env.TAURI_ENV_DEBUG === "true" ? "debug" : "release";
const targetRoots = [
  process.env.CARGO_TARGET_DIR,
  path.join(srcTauriDir, "target"),
].filter(Boolean);

const profileCandidates = findMetallibs(targetRoots, [profile]);
const fallbackCandidates = profileCandidates.length
  ? profileCandidates
  : findMetallibs(targetRoots, ["release", "debug"]);

if (!fallbackCandidates.length) {
  console.error(
    [
      "Unable to find mlx.metallib generated by qwen3_asr.",
      `Looked under: ${targetRoots.join(", ")}`,
      "Run `pnpm tauri build` once so Cargo builds the MLX backend before bundling.",
    ].join("\n"),
  );
  process.exit(1);
}

const metallibPath = newestFile(fallbackCandidates);
fs.mkdirSync(path.dirname(bundledMetallibPath), { recursive: true });
fs.copyFileSync(metallibPath, bundledMetallibPath);

console.log(`Prepared ${path.relative(repoRoot, bundledMetallibPath)} from ${path.relative(repoRoot, metallibPath)}.`);

function findMetallibs(targetRoots, profiles) {
  const candidates = [];

  for (const targetRoot of targetRoots) {
    for (const profile of profiles) {
      const buildDir = path.join(targetRoot, profile, "build");
      if (!fs.existsSync(buildDir)) {
        continue;
      }

      for (const entry of fs.readdirSync(buildDir, { withFileTypes: true })) {
        if (!entry.isDirectory() || !entry.name.startsWith("qwen3_asr-")) {
          continue;
        }

        const metallibPath = path.join(buildDir, entry.name, "out", "lib", "mlx.metallib");
        if (fs.existsSync(metallibPath)) {
          candidates.push(metallibPath);
        }
      }
    }
  }

  return [...new Set(candidates)];
}

function newestFile(paths) {
  return paths
    .map((filePath) => ({ filePath, mtimeMs: fs.statSync(filePath).mtimeMs }))
    .sort((left, right) => right.mtimeMs - left.mtimeMs)[0].filePath;
}
